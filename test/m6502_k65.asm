MOREN1: DEY
BNE BLTLP
LDA (HIGHTR),Y ;MOVE LAST OF THE BLOCK.
STA (HIGHDS),Y
DECBLT: DEC HIGHTR+1
DEC HIGHDS+1 ;START ON NEW BLOCKS.
DEX
BNE MOREN1
RTS ;RETURN TO CALLER.

